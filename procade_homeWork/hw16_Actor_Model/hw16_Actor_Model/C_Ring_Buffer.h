
#ifndef __C_RING_BUFFER_H__
#define __C_RING_BUFFER_H__

// TMI: ХЌЗЁНК ЦїРЮХЭ ИтЙі КЏМі -> СіПЊКЏМіПЁ РњРхЧЯПЉ СЂБйЧЯДТ ЙцНФ 
//	СїСЂ СЂБй: ИХ МјАЃ ОюМРКэИЎ 2Сй + АЊ РњРхЧЯДТ 1Сй == Уб 3Сй 
//	СіПЊ КЏМі: СіПЊ КЏМіПЁ РњРх 3Сй + АЊ РњРхЧЯДТ 1Сй == Уб 4Сй 
//		But!!!!!
//		УжСОРћРЮ РњРх == СїСЂ СЂБйЧЯПЉ КЏМіИІ РњРх Уб 3СйРЬ УпАЁЕШДй. 
// ==> 3 + 3 + 3 = 9
// ==> 4 + 1 + 3 = 8
// ДмМјЧЯАд АшЛъЧЯИщ 3Йј РЬЛѓ СЂБйЧЯПЉ РњРхЧбДйИщ СіПЊ КЏМіПЁ РњРхЧЯПЉ СЂБйЧЯДТ АЭРЬ РЬЕцРЬДй. 

constexpr size_t df_C_RING_BUFFER_DEFAULT_LEN = 10000;

class C_RING_BUFFER
{
private:
	char* _Buffer;

	size_t _In;
	size_t _Out;

	//-----------------------------------------------------------------------
	// idx УЪБтАЊРЬ 0РЬ ОЦДЯЖѓИщ, АќИЎЧЯБтАЁ ОюЗЦДй.
	//char* _In;
	//char* _Out;
	// const char* _Buffer_End;

	//-----------------------------------------------------------------------
	// АэЙЮ!!!! ЛчПыСпРЮ АјАЃРЬЖћ Кѓ АјАЃРК ЛчНЧ ЧЯГЊЗЮ ИИЕщ Мі РжРН
	// РЬАЩ БтЗЯЧЯДТ АњСЄ -> ЕЅРЬХЭ ГжАэ ЛЌЖЇ ИЖДй ПЌЛъЧиОп Чд. 
	// ПЌЛъЗЎРЬ ИЙОЦСњАХ ААРН
	// БИЧіЧЯДй РћАд ОВДТАЭРЛ ИоРЮРИЗЮ ОВБтЗЮ Чд.
	size_t _Full_Size;
	size_t _Use_Size;
	// int _Free_Size;	// РЬАХ ИХЙј РњРхЧЯДТАд Дѕ ДРИЎДй.

public:
	/////////////////////////////////////////////////////////////////////////
	// БтКЛ ЛчРЬСюИІ 1ИИ ByteЗЮ ЧвДчЧбДй.
	/////////////////////////////////////////////////////////////////////////
	C_RING_BUFFER(void);
	C_RING_BUFFER(int i_Buffer_Size);

	virtual ~C_RING_BUFFER();

	size_t GetBufferSize(void) { return _Full_Size; }

	/////////////////////////////////////////////////////////////////////////
	// ЧіРч ЛчПыСпРЮ ПыЗЎ ОђБт.
	//
	// Parameters: ОјРН.
	// Return: (int)ЛчПыСпРЮ ПыЗЎ.
	/////////////////////////////////////////////////////////////////////////
	size_t	GetUseSize(void) { return _Use_Size; }

	/////////////////////////////////////////////////////////////////////////
	// ЧіРч ЙіЦлПЁ ГВРК ПыЗЎ ОђБт. 
	//
	// Parameters: ОјРН.
	// Return: (int)ГВРКПыЗЎ.
	/////////////////////////////////////////////////////////////////////////
	size_t	GetFreeSize(void) { return _Full_Size - _Use_Size; }

	/////////////////////////////////////////////////////////////////////////
	// WritePos ПЁ ЕЅРЬХИ ГжРН.
	// ГВРК ЙіЦлРЧ ХЉБтАЁ iSizeКИДй РлРК АцПь ЕЅРЬХЭИІ ГжСі ОЪАэ 0РЛ ЙнШЏЧбДй.
	//
	// Parameters: (char *)ЕЅРЬХИ ЦїРЮХЭ. (int)ХЉБт. 
	// Return: (int)ГжРК ХЉБт.
	/////////////////////////////////////////////////////////////////////////
	size_t Enqueue(const char* chpData, size_t iSize);
	
	/////////////////////////////////////////////////////////////////////////
	// ReadPos ПЁМ­ ЕЅРЬХИ АЁСЎПШ. ReadPos РЬЕП.
	//
	// Parameters: (char *)ЕЅРЬХИ ЦїРЮХЭ. (int)ХЉБт.
	// Return: (int)АЁСЎПТ ХЉБт.
	/////////////////////////////////////////////////////////////////////////
	size_t	Dequeue(char* chpDest, size_t iSize);

	/////////////////////////////////////////////////////////////////////////
	// ReadPos ПЁМ­ ЕЅРЬХИ РаОюПШ. ReadPos АэСЄ.
	//
	// flag	== true: if(iSize > _Use_Size ) return 0;
	// 
	// Parameters: (char *)ЕЅРЬХИ ЦїРЮХЭ. (int)ХЉБт. (bool)defualt = false
	// Return: (int)АЁСЎПТ ХЉБт.
	/////////////////////////////////////////////////////////////////////////
	size_t	Peek(char* chpDest, size_t iSize, bool flag = false);

	/////////////////////////////////////////////////////////////////////////
	// ЙіЦлРЧ И№Еч ЕЅРЬХИ ЛшСІ.
	//
	// Parameters: ОјРН.
	// Return: ОјРН.
	/////////////////////////////////////////////////////////////////////////
	void ClearBuffer(void) { _In = _Out = 0; _Use_Size = 0; }





	/////////////////////////////////////////////////////////////////////////
	// friend class CLASS 
	// ИІ ЛчПыЧЯПЉ private ЧдМіИІ ЛчПыЧв Мі РжДй.
	/////////////////////////////////////////////////////////////////////////
public:
	/////////////////////////////////////////////////////////////////////////
	// ЙіЦл ЦїРЮХЭЗЮ ПмКЮПЁМ­ ЧбЙцПЁ РаАэ, ОЕ Мі РжДТ БцРЬ.
	// (ВїБтСі ОЪРК БцРЬ)
	//
	// ПјЧќ ХЅРЧ БИСЖЛѓ ЙіЦлРЧ ГЁДмПЁ РжДТ ЕЅРЬХЭДТ ГЁ -> УГРНРИЗЮ ЕЙОЦАЁМ­
	// 2ЙјПЁ ЕЅРЬХЭИІ ОђАХГЊ ГжРЛ Мі РжРН. РЬ КЮКаПЁМ­ ВїОюСіСі ОЪРК БцРЬИІ РЧЙЬ
	//
	// Parameters: ОјРН.
	// Return: (int)ЛчПыАЁДЩ ПыЗЎ.
	////////////////////////////////////////////////////////////////////////
	size_t	DirectEnqueueSize(void)
	{
		size_t Full_Size = _Full_Size;
		size_t In = _In;
		size_t Out = _Out;
		// EnqueueРЧ АцПь _In ЙйЗЮ ДйРНРЬ _OutРЮ АцПь ВЫ Тљ АцПьРЬДй. 
		// if (_Use_Size == 0) return 0;
		if ((In + 1) % Full_Size == Out)
			return 0;

		if (In <= ((Out + Full_Size - 1) % Full_Size))
			return ((Out + Full_Size - 1) % Full_Size) - In;
		else if (In >= Out)
			return Full_Size - In;

		return 0;
	}
	size_t	DirectDequeueSize(void)
	{
		if (_In >= _Out)
		{
			return _In - _Out;
		}
		else
		{
			return _Full_Size - _Out;
		}
	}

	/////////////////////////////////////////////////////////////////////////
	// ПјЧЯДТ БцРЬИИХ­ РаБтРЇФЁ ПЁМ­ ЛшСІ / ОВБт РЇФЁ РЬЕП
	//
	// Parameters: ОјРН.
	// Return: (int)РЬЕПХЉБт
	/////////////////////////////////////////////////////////////////////////
	size_t	MoveRear(size_t iSize)
	{
		_Use_Size += iSize;
		_In = (_In + iSize) % _Full_Size;
		return _In;
	}
	size_t	MoveFront(size_t iSize)
	{
		_Use_Size -= iSize;
		_Out = (_Out + iSize) % _Full_Size;
		return _Out;
	}



	/////////////////////////////////////////////////////////////////////////
	// ЙіЦлРЧ Front, _Out ЦїРЮХЭ ОђРН.
	//
	// Parameters: ОјРН.
	// Return: (char *) ЙіЦл ЦїРЮХЭ.
	/////////////////////////////////////////////////////////////////////////
	char* GetFrontBufferPtr(void)
	{
		return _Buffer + _Out;
	}


	/////////////////////////////////////////////////////////////////////////
	// ЙіЦлРЧ RearPos, _In ЦїРЮХЭ ОђРН.
	//
	// Parameters: ОјРН.
	// Return: (char *) ЙіЦл ЦїРЮХЭ.
	/////////////////////////////////////////////////////////////////////////
	char* GetRearBufferPtr(void)
	{
		return _Buffer + _In;
	}

};


#endif // !__C_RING_BUFFER_H__

